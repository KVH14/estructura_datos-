#Listas_Enlazadas-Son estructuras lineales formdo por nodos -tiene un dato y un puntero al siguiente nodo
#La lista doblemente enlazada es una lista enlazada en la que cada nodo tiene un puntero al nodo anterior y al siguiente hay un recorrido en ambas direcciones
#La lista circular simple es una lista enlazada en la que el último nodo apunta al primero, formando un ciclo
#La lista circular doblemente enlazada es una lista enlazada en la que el último nodo apunta al primero y el primer nodo al último, formando un ciclo bidireccional

#PILAS
#Una pila es una estructura de datos lineal que sigue el principio LIFO (Last In, First Out), es decir, el último elemento en entrar es el primero en salir.}
#El primero que llega va a estar en la parte inferioir-tienes espacio para dato y el apuntador al siguiente nodo y va hacia abajo
